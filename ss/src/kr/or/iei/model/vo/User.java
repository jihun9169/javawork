package kr.or.iei.model.vo;

public class User {
	private String id;

	private int pwd;

	private String name;

	private int age;

	private long  phone;

	public User(String id, int pwd, String name, int age, long phone) {
		this.id = id;
		this.pwd = pwd;
		this.name = name;
		this.age = age;
		this.phone = phone;
	}








	public String getId() {

	return id;

	}

	public void setId(String id) {

	this.id = id;

	}

	public int getPwd() {

	return pwd;

	}

	public void setPwd(int pwd) {

	this.pwd = pwd;

	}

	public String getName() {

	return name;

	}

	public void setName(String name) {

	this.name = name;

	}

	public int getAge() {

	return age;

	}

	public void setAge(int age) {

	this.age = age;

	}

	public long getPhone() {

	return phone;

	}

	public void setPhone(long phone) {

	this.phone = phone;

	}

	
	@Override
	public String toString() {

	return  id + "  "+"pass" + pwd + "  "+  name  + "     "+age + "  "+  phone ; 

	}

	}
