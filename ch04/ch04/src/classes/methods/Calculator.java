package classes.methods;

// 계산기 자료형 정의
public class Calculator {
	// 메서드 정의 - 더하기, 뺴기, 곱하기, 나누기
	public int add(int n1, int n2) {
		return n1 + n2;
	}
	
	public int sub (int n1, int n2) {
		return n1 - n2;
	}
	public int mul (int n1, int n2) {
		return n1 * n2;
	
}
	public int div (int n1, int n2) {
		return n1 / n2;
}
}